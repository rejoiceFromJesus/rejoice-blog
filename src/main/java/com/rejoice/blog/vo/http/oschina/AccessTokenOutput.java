package com.rejoice.blog.vo.http.oschina;

public class AccessTokenOutput {
	
	 private String access_token;
	 private String efresh_token;
	 private String id;
	 private String oken_type;
	 private String xpires_in;
	
	public String getAccess_token() {
		return access_token;
	}
	public void setAccess_token(String access_token) {
		this.access_token = access_token;
	}
	public String getEfresh_token() {
		return efresh_token;
	}
	public void setEfresh_token(String efresh_token) {
		this.efresh_token = efresh_token;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getOken_type() {
		return oken_type;
	}
	public void setOken_type(String oken_type) {
		this.oken_type = oken_type;
	}
	public String getXpires_in() {
		return xpires_in;
	}
	public void setXpires_in(String xpires_in) {
		this.xpires_in = xpires_in;
	}
	 
	
}
